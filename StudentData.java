package App;

public class StudentData {
    String name;
    String entryNumber;
    String email;
    String contactNumber;
    String homeCity;

    public StudentData(String name, String entryNumber, String email, String contactNumber, String homeCity) {
        this.name = name;
        this.entryNumber = entryNumber;
        this.email = email;
        this.contactNumber = contactNumber;
        this.homeCity = homeCity;
    }
}
